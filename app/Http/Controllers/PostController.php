     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Validate and Store the Blog Post...


      $request->validate([
          'title' => 'required|unique:posts|max:255',
          'body' => 'required',
      ]);

        Post::create([
          'title' => request('title'),
          'body' => request('body')
      ]);

        return redirect('/posts');
    }

